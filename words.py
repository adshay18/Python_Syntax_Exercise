def print_upper_words(words_list):
    """
    Given a list of words print each word.
    The words printed must be converted to UPPERCASE letters!
    Ex:    print_upper_words(["hi", "wow"])
            HI
            WOW
    """

    for word in words_list:
        print(word.upper())

